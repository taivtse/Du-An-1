    private void btnXacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXacNhanActionPerformed
        String soDienThoai = txtSoDienThoai.getText();
        ShareHelper.USER = new NguoiDungDaoImpl().getBySoDienThoai(soDienThoai);
        if (ShareHelper.USER == null) {
            DialogHelper.message(this, "Không tồn tại người dùng có số điện thoại: " + soDienThoai, DialogHelper.ERROR_MESSAGE);
        } else {
            String randomMaXacNhan = StringUtil.randomNumber(5);
            boolean isSent = this.guiMaXacNhan(randomMaXacNhan);
            if (isSent) {
                DialogHelper.message(this, "Đã gửi mã xác nhận\nVui lòng nhập mã xác nhận và tiến hành đổi mật khẩu", DialogHelper.INFORMATION_MESSAGE);

                boolean isSaved = this.luuMaXacNhan(randomMaXacNhan);
                if (isSaved) {
                    String inputMaXacNhan;
                    boolean isCorrectMaXacNhan = false;
                    do {
                        inputMaXacNhan = DialogHelper.prompt(this, "Nhập mã xác nhận");
                        isCorrectMaXacNhan = randomMaXacNhan.equals(inputMaXacNhan);
                        if (inputMaXacNhan == null || isCorrectMaXacNhan) {
                            break;
                        } else {
                            DialogHelper.message(this, "Mã xác nhận không đúng!\nVui lòng kiểm tra lại", DialogHelper.ERROR_MESSAGE);
                        }
                    } while (true);

                    if (isCorrectMaXacNhan) {
                        this.dispose();
                        new DialogDatLaiMatKhau((Frame) this.getOwner(), true).setVisible(true);
                    } else {
                        DialogHelper.message(this, "Mã xác nhận đã hết hạn!\nVui lòng nhận lại mã mới", DialogHelper.ERROR_MESSAGE);
                    }

                    this.xoaMaXacNhan();
                } else {
                    DialogHelper.message(this, "Đã xảy ra lỗi!\nVui lòng thử lại sau", DialogHelper.ERROR_MESSAGE);
                }

            } else {
                DialogHelper.message(this, "Gửi mã xác nhận thất bại\nVui lòng kiểm tra lại số điện thoại", DialogHelper.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnXacNhanActionPerformed

